class CitationManager:
    def __init__(self, references: List[Dict[str, str]]):
        self.references = {item["key"]: item["entry"] for item in references}
        self.order: OrderedDict[str, int] = OrderedDict()

    def cite(self, key: str) -> str:
        if key not in self.references:
            raise KeyError(f"Unknown reference key: {key}")
        if key not in self.order:
            self.order[key] = len(self.order) + 1
        return f"[{self.order[key]}]"

    def used_entries(self) -> List[Tuple[int, str]]:
        return [(number, self.references[key]) for key, number in self.order.items()]


class CrossRefManager:
    def __init__(self):
        self.figures = OrderedDict()
        self.tables = OrderedDict()
        self.equations = OrderedDict()

    def fig(self, key: str) -> str:
        if key not in self.figures:
            self.figures[key] = len(self.figures) + 1
        return f"图{self.figures[key]}"

    def table(self, key: str) -> str:
        if key not in self.tables:
            self.tables[key] = len(self.tables) + 1
        return f"表{self.tables[key]}"

    def eq(self, key: str) -> str:
        if key not in self.equations:
            self.equations[key] = len(self.equations) + 1
        return f"式({self.equations[key]})"

    def as_dict(self) -> Dict[str, Dict[str, int]]:
        return {
            "figures": dict(self.figures),
            "tables": dict(self.tables),
            "equations": dict(self.equations),
        }


def load_json(path: Path) -> Dict[str, Any]:
    if not path.exists():
        raise FileNotFoundError(f"Required simulation artifact missing: {path}")
    return json.loads(path.read_text(encoding="utf-8"))


def load_json_optional(path: Path) -> Dict[str, Any]:
    if not path.exists():
        return {}
    return json.loads(path.read_text(encoding="utf-8"))


def load_csv_rows(path: Path) -> List[Dict[str, str]]:
    if not path.exists():
        raise FileNotFoundError(f"Required simulation artifact missing: {path}")
    with path.open("r", encoding="utf-8-sig", newline="") as f:
        return list(csv.DictReader(f))


def fnum(value: Any, digits: int = 2) -> str:
    try:
        v = float(value)
    except (TypeError, ValueError):
        return str(value)
    if math.isnan(v):
        return "未达到"
    return f"{v:.{digits}f}"


def set_run_font(run, size: float = 12, bold: bool = False):
    run.font.name = "Times New Roman"
    run._element.rPr.rFonts.set(qn("w:eastAsia"), "宋体")
    run.font.size = Pt(size)
    run.bold = bold


def add_text(paragraph, text: str, size: float = 12, bold: bool = False):
    run = paragraph.add_run(text)
    set_run_font(run, size=size, bold=bold)
    return run


def add_paragraph(doc: Document, text: str, body_parts: List[str] | None = None, first_line: bool = True):
    p = doc.add_paragraph()
    p.paragraph_format.line_spacing = 1.5
    p.paragraph_format.space_after = Pt(4)
    if first_line:
        p.paragraph_format.first_line_indent = Pt(24)
    add_text(p, text)
    if body_parts is not None:
        body_parts.append(text)
    return p


def add_heading(doc: Document, text: str, level: int):
    p = doc.add_paragraph()
    p.paragraph_format.space_before = Pt(8)
    p.paragraph_format.space_after = Pt(5)
    if level == 1:
        size = 15
    elif level == 2:
        size = 13
    else:
        size = 12
    add_text(p, text, size=size, bold=True)
    return p


def add_caption(doc: Document, text: str):
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    p.paragraph_format.space_after = Pt(6)
    add_text(p, text, size=10.5)
    return p


def add_figure(doc: Document, xref: CrossRefManager, key: str, image_name: str, caption: str):
    label = xref.fig(key)
    path = FIGURES_DIR / image_name
    if not path.exists():
        raise FileNotFoundError(f"Referenced figure missing: {path}")
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = p.add_run()
    run.add_picture(str(path), width=Cm(14.8))
    add_caption(doc, f"{label} {caption}")


def add_table_caption(doc: Document, xref: CrossRefManager, key: str, caption: str):
    label = xref.table(key)
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    add_text(p, f"{label} {caption}", size=10.5)
    return label


def add_simple_table(doc: Document, headers: Iterable[str], rows: Iterable[Iterable[str]]):
    table = doc.add_table(rows=1, cols=len(list(headers)))
    table.style = "Table Grid"
    hdr = table.rows[0].cells
    headers_list = list(headers)
    for idx, h in enumerate(headers_list):
        p = hdr[idx].paragraphs[0]
        p.alignment = WD_ALIGN_PARAGRAPH.CENTER
        add_text(p, h, bold=True)
    for row in rows:
        cells = table.add_row().cells
        for idx, value in enumerate(row):
            p = cells[idx].paragraphs[0]
            add_text(p, str(value), size=10.5)
    return table


def add_equation(doc: Document, xref: CrossRefManager, key: str, equation: str, body_parts: List[str]):
    label = xref.eq(key)
    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    text = f"{equation}    {label}"
    add_text(p, text, size=11)
    body_parts.append(text)
    return label


def add_page_number(section):
    footer = section.footer.paragraphs[0]
    footer.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = footer.add_run()
    fld_char1 = OxmlElement("w:fldChar")
    fld_char1.set(qn("w:fldCharType"), "begin")
    instr = OxmlElement("w:instrText")
    instr.set(qn("xml:space"), "preserve")
    instr.text = "PAGE"
    fld_char2 = OxmlElement("w:fldChar")
    fld_char2.set(qn("w:fldCharType"), "end")
    run._r.append(fld_char1)
    run._r.append(instr)
    run._r.append(fld_char2)
    set_run_font(run, size=10.5)


def count_cjk(text: str) -> int:
    return len(re.findall(r"[\u4e00-\u9fff]", text))


def release_label(version_text: str) -> str:
    match = re.search(r"\((R\d{4}[ab])\)", version_text or "")
    if match:
        return match.group(1)
    return version_text or "未记录"


